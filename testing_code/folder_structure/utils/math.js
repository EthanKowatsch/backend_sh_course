/**
 * Function to add two numbers
 * 
 * @param {number} x 
 * @param {number} y 
 * @returns The sum of the x and y variable.
 */
export function add(x, y) {
    return x + y;
}

/**
 * Function to subtract two numbers
 * 
 * @param {number} x 
 * @param {number} y 
 * @returns Returns the difference between x and y.
 */
export function subtract(x, y) {
    return x - y;
}